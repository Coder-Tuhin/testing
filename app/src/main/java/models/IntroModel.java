public class IntroModel {
    private int introImage;
    private int introTitle;
    private int introDesc;

    public int getIntroImage() {
        return introImage;
    }

    public void setIntroImage(int introImage) {
        this.introImage = introImage;
    }

    public int getIntroTitle() {
        return introTitle;
    }

    public void setIntroTitle(int introTitle) {
        this.introTitle = introTitle;
    }

    public int getIntroDesc() {
        return introDesc;
    }

    public void setIntroDesc(int introDesc) {
        this.introDesc = introDesc;
    }
}
